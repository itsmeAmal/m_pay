package com.caller.components;

public class ProfileType {
	int superagentCount;
	int distributorCount;
	int agentCount;
	int subscriberCount;
	
	public int getSuperagentCount() {
		return superagentCount;
	}
	public void setSuperagentCount(int superagentCount) {
		this.superagentCount = superagentCount;
	}
	public int getDistributorCount() {
		return distributorCount;
	}
	public void setDistributorCount(int distributorCount) {
		this.distributorCount = distributorCount;
	}
	public int getAgentCount() {
		return agentCount;
	}
	public void setAgentCount(int agentCount) {
		this.agentCount = agentCount;
	}
	public int getSubscriberCount() {
		return subscriberCount;
	}
	public void setSubscriberCount(int subscriberCount) {
		this.subscriberCount = subscriberCount;
	} 

	


}
